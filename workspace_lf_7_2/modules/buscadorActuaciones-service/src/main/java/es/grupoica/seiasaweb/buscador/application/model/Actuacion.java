package es.grupoica.seiasaweb.buscador.application.model;

public class Actuacion {
	private String entryId;
	private String codTecnico;
	private String nombre;
	private String ubicacion;
	private String comunidadAutonoma;
	private String provincia;
	private String superficie;
	private String regantes;
	private String cultivos;
	private String situacion;
	private String colorSituacion;
	private String longitud;
	private String latitud;
	private String urlTitle;
	
	public String getEntryId() {
		return entryId;
	}
	public void setEntryId(String entryId) {
		this.entryId = entryId;
	}
	public String getCodTecnico() {
		return codTecnico;
	}
	public void setCodTecnico(String codTecnico) {
		this.codTecnico = codTecnico;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getUbicacion() {
		return ubicacion;
	}
	public void setUbicacion(String ubicacion) {
		this.ubicacion = ubicacion;
	}
	public String getComunidadAutonoma() {
		return comunidadAutonoma;
	}
	public void setComunidadAutonoma(String comunidadAutonoma) {
		this.comunidadAutonoma = comunidadAutonoma;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getSuperficie() {
		return superficie;
	}
	public void setSuperficie(String superficie) {
		this.superficie = superficie;
	}
	public String getRegantes() {
		return regantes;
	}
	public void setRegantes(String regantes) {
		this.regantes = regantes;
	}
	public String getCultivos() {
		return cultivos;
	}
	public void setCultivos(String cultivos) {
		this.cultivos = cultivos;
	}
	public String getSituacion() {
		return situacion;
	}
	public void setSituacion(String situacion) {
		this.situacion = situacion;
	}
	public String getColorSituacion() {
		return colorSituacion;
	}
	public void setColorSituacion(String colorSituacion) {
		this.colorSituacion = colorSituacion;
	}
	public String getLongitud() {
		return longitud;
	}
	public void setLongitud(String longitud) {
		this.longitud = longitud;
	}
	public String getLatitud() {
		return latitud;
	}
	public void setLatitud(String latitud) {
		this.latitud = latitud;
	}
	public String getUrlTitle() {
		return urlTitle;
	}
	public void setUrlTitle(String urlTitle) {
		this.urlTitle = urlTitle;
	}
	
	
	
	
}
